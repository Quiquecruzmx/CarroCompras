    public void cambiarArticulo(int posicion){
        if(posicion == -1){
            this.i = articulos.length-1;
        }else if(posicion == articulos.length ){
            this.i = 0;
        }else{ this.i=posicion; }
        
        jLabel1.setText(articulos[i].getNombre());
        jLabel2.setText(articulos[i].getDescripcion());
        jLabel3.setText(articulos[i].getMarca());
        jLabel4.setText("Precio: $"+articulos[i].getPrecio());
        jLabel5.setText("# Existencias: "+articulos[i].getExistencias());
        Icon icono = new ImageIcon(articulos[i].getFoto_producto().getImage().getScaledInstance(200, 230, Image.SCALE_DEFAULT));
        jLabel7.setIcon(icono);
    }
    
