    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if(articulos[i].getExistencias()!=0){
            articulos[i].setExistencias(articulos[i].getExistencias()-1);
            jLabel5.setText("# Existencias: "+articulos[i].getExistencias());
            
            if(agregados==null){
                agregados = new int[1];
                agregados[0]= articulos[i].getId()-1;
            }else
                agregados = addElement(agregados, articulos[i].getId()-1);
            
            MostrarAgregados();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    static int[] addElement(int[] O, int e) {
        O  = Arrays.copyOf(O, O.length + 1);
        O[O.length - 1] = e;
        return O;
    }
    
    private void MostrarAgregados(){
        int n=0;
        final DefaultListModel model = new DefaultListModel();
        while(n < agregados.length){
            model.addElement(articulos[agregados[n]].getNombre()+ " $"+articulos[agregados[n]].getPrecio());
            n++;
        }
        jList1.setModel(model);
    }
    
    
